(function applyFamilies() {
  const byId = {};
  Object.entries(PLANT_FAMILIES).forEach(([fam, ids]) =>
    ids.forEach(id => { byId[id] = fam; })
  );
  BUILTIN_PLANTS.forEach(p => { if (!p.family) p.family = byId[p.id] || null; });
})();

(function applyGerminationDefaults() {
  BUILTIN_PLANTS.forEach(p => {
    const range = GERMINATION_DEFAULTS[p.id];
    if (!range) return;
    if (p.germinationDaysMin === undefined || p.germinationDaysMin === null) p.germinationDaysMin = range[0];
    if (p.germinationDaysMax === undefined || p.germinationDaysMax === null) p.germinationDaysMax = range[1];
  });
})();
