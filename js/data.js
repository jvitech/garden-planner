'use strict';

// Each plant:
//   id, name, emoji, cat (vegetable|herb|fruit|flower)
//   spacing: 'WCm×HCm' — the plant's own footprint (space taken by one plant).
//            Drives the grid block size. Small plants (<10 cm) get density >1
//            shown as "×N" in the bed grid (N = floor(10/w)*floor(10/h)).
//   rowSpacing: informational gap in cm between rows. Displayed in UI metadata,
//            but not used in automatic plant count calculations.
//   cellsNeeded: approximate grid cells the plant block occupies (informational)
//   family: botanical family — assigned via PLANT_FAMILIES map below
//   sun: 'full' | 'partial' | 'shade'
//   water: 'low' | 'medium' | 'high'
//   height: 'low' | 'medium' | 'tall' | 'vine'
//   daysToHarvest: string
//   sowIndoor:  months (1-based) to start seeds indoors / under cover (null = not applicable)
//   sowOutdoor: months to sow directly outside or transplant outdoors
//   harv: months to harvest
//   good: companion plant ids (benefits)
//   bad:  companion plant ids (allelopathy / pests)
//   notes: short growing tip

// Half-month helper: H('3-6') → [3,3.5,…,6,6.5]  H('3,7-8') → [3,3.5,7,7.5,8,8.5]
// Whole integer endpoint includes both halves of that month.
function H(str) {
  const result = new Set();
  for (const token of (str || '').split(',')) {
    const t = token.trim();
    if (!t) continue;
    const parts = t.split('-');
    if (parts.length === 2) {
      const from = parseFloat(parts[0]);
      const to   = parseFloat(parts[1]);
      const end  = Number.isInteger(to) ? to + 0.5 : to;
      for (let h = from; h <= end + 1e-9; h = Math.round((h + 0.5) * 10) / 10) result.add(h);
    } else {
      const v = parseFloat(t);
      if (Number.isInteger(v)) { result.add(v); result.add(v + 0.5); }
      else result.add(v);
    }
  }
  return [...result].sort((a, b) => a - b);
}

